#Aritmetic Operators#

$numero1 = 12;
$numero2 = 5;

#sum
$total = $numero1 + $numero2;
print "\n";
print "sum: " . $total . "\n";

#subtract
$total = $numero1 - $numero2;
print "\n";
print "subtract: " . $total . "\n";

#multiply
$total = $numero1 * $numero2;
print "\n";
print "mulitply: " . $total . "\n";

#divide
$total = $numero1 / $numero2;
print "\n";
print "divide: " . $total . "\n";

#concatenate
$total = $numero1 . $numero2;
print "\n";
print "concatenate: " . $total . "\n";

#exponencial
$total = $numero1 ** $numero2;
print "\n";
print "exponencial: " . $total . "\n";

#rest of division
$total = $numero1 % $numero2;
print "\n";
print "rest: " . $total . "\n";

#repeat of char
$total = $numero1 x $numero2;
print "\n";
print "repeat x: " . $total . "\n";

